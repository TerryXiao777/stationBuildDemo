package com.demo.bean;

public class Temple {
   private String temName;

   private String temIndex;

   private String temTop;

   private String temLeft;

   private String temType;

   private String temContent;

   private String temImages;

   private String tem;

    public String getTemName() {
        return temName;
    }

    public void setTemName(String temName) {
        this.temName = temName;
    }

    public String getTemIndex() {
        return temIndex;
    }

    public void setTemIndex(String temIndex) {
        this.temIndex = temIndex;
    }

    public String getTemTop() {
        return temTop;
    }

    public void setTemTop(String temTop) {
        this.temTop = temTop;
    }

    public String getTemLeft() {
        return temLeft;
    }

    public void setTemLeft(String temLeft) {
        this.temLeft = temLeft;
    }

    public String getTemType() {
        return temType;
    }

    public void setTemType(String temType) {
        this.temType = temType;
    }

    public String getTemContent() {
        return temContent;
    }

    public void setTemContent(String temContent) {
        this.temContent = temContent;
    }

    public String getTemImages() {
        return temImages;
    }

    public void setTemImages(String temImages) {
        this.temImages = temImages;
    }

    public String getTem() {
        return tem;
    }

    public void setTem(String tem) {
        this.tem = tem;
    }

}
